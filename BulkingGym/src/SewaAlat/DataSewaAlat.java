package SewaAlat;

/**
 *
 * @author Lab Informatika
 */
public class DataSewaAlat {
    private String penyewa;
    private String namaAlat;
    private String noHP;
    private int waktuSewa;
    private int hargaSewa;

    public String getPenyewa() {
        return penyewa;
    }

    public void setPenyewa(String penyewa) {
        this.penyewa = penyewa;
    }

    public String getNamaAlat() {
        return namaAlat;
    }

    public void setNamaAlat(String namaAlat) {
        this.namaAlat = namaAlat;
    }

    public String getNoHP() {
        return noHP;
    }

    public void setNoHP(String noHP) {
        this.noHP = noHP;
    }

    public int getWaktuSewa() {
        return waktuSewa;
    }

    public void setWaktuSewa(int waktuSewa) {
        this.waktuSewa = waktuSewa;
    }

    public int getHargaSewa() {
        return hargaSewa;
    }

    public void setHargaSewa(int hargaSewa) {
        this.hargaSewa = hargaSewa;
    }
    
    
}
